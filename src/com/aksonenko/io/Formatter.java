package com.aksonenko.io;

public class Formatter {
	
	public static void main(String[] args) {
        double pi = Math.PI;
        
        System.out.printf("%.1f", pi);
        System.out.println();
        System.out.printf("%.2f", pi);
        System.out.println();
        System.out.printf("%.3f", pi);
        System.out.println();
        System.out.printf("%.4f", pi);
        System.out.println();
        System.out.printf("%.5f", pi); 
    }

}
